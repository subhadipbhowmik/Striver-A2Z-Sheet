{

    // https://www.codingninjas.com/studio/problems/if-else-decision-making_8357235
    // Write your code here
    if (a > b)
    {
        return "greater";
    }
    else if (b > a)
    {
        return "smaller";
    }

    else
    {
        return "equal";
    }
}
